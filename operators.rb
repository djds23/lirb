module Operator
    def Operator.add(a, b)
        a + b
    end

    def Operator.sub(a, b)
        a - b
    end

    def Operator.mul(a, b)
        a * b
    end

    def Operator.div(a, b)
        a / b
    end

    def Operator.gt(a, b)
        a > b
    end

    def Operator.lt(a, b)
        a < b
    end

    def Operator.gte(a, b)
        a >= b
    end

    def Operator.lte(a, b)
        a <= b
    end

    def Operator.eq(a, b)
        a == b
    end
end
